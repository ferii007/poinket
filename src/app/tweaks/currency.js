const currencyFormatter = (value = 0, currency = "USD", locale = "en") => {
    let options = {
        style: "currency",
        currency: currency,
    };

    if (fractionDigits[currency]) {
        options.minimumFractionDigits = fractionDigits[currency].min;
        options.maximumFractionDigits = fractionDigits[currency].max;
    } else {
        locale = "en";
        options.currency = "USD"; 
        options.minimumFractionDigits = 2;
        options.maximumFractionDigits = 2;
    }

    const formatCurrency = new Intl.NumberFormat(locale, options).format(value);

    return formatCurrency;
}

